package data_types;

public class PrimitiveDataTypes {
    public static void main(String[] args) {

//1 Plain declaration
       byte numberLikes;
       int numberOfComments;
       double price;
       char gender;
       long numberOfViews;
       float shoeSize;

       //2 Declaration + initalization(assigning a value to variable for first time)
        byte elonMuskAge = 52;
        short mObamaAge = 50;
        double johnDoeAge = 28;

        System.out.println(elonMuskAge); //Elon Musk
        System.out.println(mObamaAge); //
        System.out.println(johnDoeAge);
//3 declare multiple variables with same data type
        int miles = 34, temp, weight, height = 6;
        temp = 45;
        weight = 175;
    }
}
